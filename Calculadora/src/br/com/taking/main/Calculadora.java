package br.com.taking.main;

public class Calculadora {

	public int somar(int o1, int o2) {
		return o1 + o2;
	}

	public int subtrair(int o1, int o2) {
		return o1 - o2;
	}

	public int multiplicar(int o1, int o2) {
		return o1 * o2;
	}

	public int dividir(int o1, int o2) {
		return o1 / o2;
	}
}
